// ============================================================================
//
// スケルトン段階: describe.todo / it.todo のみ、本実装は Phase 2 で。
// 既存のテストヘルパーを後で共有に切り出す候補。
// ============================================================================

import { describe, it } from 'vitest';

// ============================================================================
// シナリオ 1: draw トリガー — 防御側の手札 +1
// ============================================================================

describe('trigger: draw', () => {
  it.todo(
    'リーダー被ダメ時、ライフカードに triggerType=draw → 防御側デッキから 1 枚手札へ、さらに通常のライフ→手札移動も行う',
  );
});

// ============================================================================
// シナリオ 2: mana トリガー — 防御側の次ターン maxCost +2
// ============================================================================

describe('trigger: mana', () => {
  it.todo(
    'triggerType=mana 発動で defender.nextTurnManaBonus=2 が立ち、次ターンの cost フェーズで calcManaForTurn 結果 +2 される (上限 15 は維持)',
  );
});

// ============================================================================
// シナリオ 3: destroy トリガー — 攻撃側の最弱キャラ 1 体破壊
// ============================================================================

describe('trigger: destroy', () => {
  it.todo(
    'triggerType=destroy 発動で attacker.board 内の最低 attackPower のキャラが graveyard へ移動、board から削除',
  );
});

// ============================================================================
// シナリオ 4: defense トリガー — 今回の攻撃を無効化
// ============================================================================

describe('trigger: defense', () => {
  it.todo(
    'triggerType=defense 発動で defender.leader.life は減らず、カードは手札へ移動、defense_blocked イベント発生、attacker はレスト状態になる',
  );
});

// ============================================================================
// シナリオ 5: revive トリガー — 墓地から 1 枚を手札へ
// ============================================================================

describe('trigger: revive', () => {
  it.todo(
    'triggerType=revive 発動で defender.graveyard[0] が手札へ戻る、空墓地なら no-op',
  );
});

// ============================================================================
// シナリオ 6: 非トリガー通常フロー
// ============================================================================

describe('trigger: null (non-trigger)', () => {
  it.todo(
    'triggerType=null のライフカードは既存フロー通り: life -1、カードは手札へ、trigger_activated イベントは出ない',
  );
});
